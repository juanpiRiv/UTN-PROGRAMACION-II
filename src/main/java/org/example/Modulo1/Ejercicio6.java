package org.example.Modulo1;

public class Ejercicio6 {
    public static void main(String[] args) {
        // Usando caracteres de escape \n (nueva línea) y \" (comillas dobles)
        System.out.println("Nombre: Juan Pérez\nEdad: 30 años\nDirección: \"Calle Falsa 123\"");

        System.out.println("\n--- Separador ---\n");

        System.out.println("Nombre: Juan Pérez");
        System.out.println("Edad: 30 años");
        System.out.println("Dirección: \"Calle Falsa 123\"");

        System.out.println("\n--- Otros caracteres de escape útiles ---");
        System.out.println("Tabulación:\tTexto con tab");
        System.out.println("Barra invertida: \\");
        System.out.println("Comilla simple: \'Hola\'");
    }
}
